import turtle
t = turtle.Turtle()

# 画正方形
def square(size, color, t):
    t.fillcolor(color)
    t.begin_fill()
    for _ in range(4):
        t.fd(size)
        t.lt(90)
    t.end_fill()

ls = [
'***********########*',
'**********##########',
'**********##*#######',
'**********##########',
'**********##########', 
'**********#####*****', 
'**********########**',
'#********#####******',
'#*******######******',
'##*****#########****',
'###***########*#****',
'##############******',
'##############******',
'*############*******',
'**##########********',
'***########*********',
'****######**********',
'****###*##**********',
'****##***#**********',
'****#****#**********',
'****##***##*********']

def draw_by_str(ls, size):
    for i in ls:
        x = t.xcor()
        y = t.ycor()
        for j in i:
            if j == '#':
                square(size, 'black', t)
            t.penup()
            t.fd(size)
            t.pendown()
        t.penup()
        t.goto(x, y - size)
        
    
draw_by_str(ls, 10)
turtle.done()